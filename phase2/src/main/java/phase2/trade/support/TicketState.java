package phase2.trade.support;

/**
 * The enum Ticket state.
 *
 * @author Dan Lyu
 */
public enum TicketState {

    /**
     * Solved ticket state.
     */
    SOLVED,
    /**
     * Combined ticket state.
     */
    COMBINED,
    /**
     * Abandoned ticket state.
     */
    ABANDONED,
    /**
     * Pending allocation ticket state.
     */
    PENDING_ALLOCATION,
    /**
     * Solving ticket state.
     */
    SOLVING
}
